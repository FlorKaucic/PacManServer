package game.logic;

public class Drawable {
	protected int posX;
	protected int posY;
	protected int width;
	protected int height;

	public Drawable(int posX, int posY, int width, int height) {
		this.posX = posX;
		this.posY = posY;
		this.width = width;
		this.height = height;
	}
	
}
